/**
 * 한자가 포함된 문자열에서 각 한자 뒤에 (한글) 표기를 추가
 * 예: "庚金" → "庚(경)金(금)"
 * 예: "丙辰/庚寅" → "丙(병)辰(진)/庚(경)寅(인)"
 * 
 * 이미 (한글)이 붙어있는 경우 중복 추가하지 않음
 */
export function addHanjaReading(text: string): string {
  if (!text) return text;
  
  let result = '';
  for (let i = 0; i < text.length; i++) {
    const char = text[i];
    const reading = HANJA_MAP[char];
    
    if (reading) {
      // 이미 괄호가 있는지 확인 (다음 문자가 '('인 경우)
      if (text[i + 1] === '(') {
        result += char;
      } else {
        result += `${char}(${reading})`;
      }
    } else {
      result += char;
    }
  }
  return result;
}

/**
 * 사주 주(柱) 표기를 읽기 쉽게 변환
 * 예: "丙辰" → "丙(병)辰(진)"
 */
export function formatPillar(pillar: string): string {
  return addHanjaReading(pillar);
}

/**
 * 사주 전체 주를 포맷
 * 예: "丙辰/庚寅/庚午/己卯" → "丙(병)辰(진) / 庚(경)寅(인) / ..."
 */
export function formatPillars(pillars: string): string {
  return pillars.split('/').map(p => addHanjaReading(p.trim())).join(' / ');
}
